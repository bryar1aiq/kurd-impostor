// Remove duplicates and convert to KurdishWord format
const UNIQUE_WORDS = [...new Set(WORDS)].map((word) => ({ word }));

export const KURDISH_WORDS: KurdishWord[] = UNIQUE_WORDS;

export function getRandomWords(count: number): KurdishWord[] {
  const shuffled = [...KURDISH_WORDS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, KURDISH_WORDS.length));
}

export function getRandomWord(): KurdishWord {
  return KURDISH_WORDS[Math.floor(Math.random() * KURDISH_WORDS.length)];
}
